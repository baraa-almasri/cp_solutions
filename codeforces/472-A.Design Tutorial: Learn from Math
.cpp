#include <iostream>
#include <stdio.h>
#include <string>
#include <string.h>
#include <math.h>
#include <vector>
#include <algorithm>

inline bool isOdd(int n) {

    return n % 2 == 1;
}

////////////////////////////////////////////////////////////////
////////////////MAIN FUNCTION///////////////////////////////////
////////////////////////////////////////////////////////////////

int main() {
    int n;
    scanf("%d", &n);

    int p1, p2;

    p1 = isOdd(n)? 9: 8;
    p2 = n - p1;

    printf("%d %d\n", p1, p2);
    
    return 0;
}

