void solve() {
    int n, k;
    scanf("%d %d", &n, &k);

    int problems{0};
    for (int i = 1; i <= n; i++) {
        if (k + 5*i <= 240) {
            problems++;
            k += 5*i;
        }
    }

    printf("%d\n", problems);
}

